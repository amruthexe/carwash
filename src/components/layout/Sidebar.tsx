"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";
import { Home, Car, CalendarDays, Receipt, User } from "lucide-react";

function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

interface SidebarItemProps {
  href: string;
  icon: React.ReactNode;
  label: string;
}

function SidebarItem({ href, icon, label }: SidebarItemProps) {
  const pathname = usePathname();
  const isActive = pathname === href;

  return (
    <Link
      href={href}
      className={cn(
        "flex items-center gap-3 w-full p-3 text-sm font-semibold rounded-lg transition-colors whitespace-nowrap",
        isActive
          ? "bg-blue-600 text-white shadow"
          : "hover:bg-slate-700 text-slate-200"
      )}
    >
      {icon} {label}
    </Link>
  );
}

